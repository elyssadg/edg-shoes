package model;

public class Shoes {
	
	private int price;
	private String id, model, brand, color;

	public Shoes(int price, String id, String model, String brand, String color) {
		super();
		this.price = price;
		this.id = id;
		this.model = model;
		this.brand = brand;
		this.color = color;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

}
